            <p><h2>Introdução – Breve memória histórica</h2>
No dia 31 de agosto de 1998,  realizou-se uma reunião para a reorganização da equipe da Caritas Arquidiocesana de Diamantina, no salão paroquial da Paróquia Sagrado Coração de Jesus, em Carbonita, coordenada pelo Pe. Elio Saldanha. Estava presente a Sra. Maria Cristina dos Anjos, representante da Caritas Regional que sugeriu o processo de institucionalização através da elaboração de um estatuto próprio e eleição de uma diretoria. No dia 13 de outubro de 1998, na Paróquia Sagrada Família de Curvelo, com a assessoria da Sra. Maria Cristina foram assumidas as prioridades da Caritas Brasileira: emergências sociais; formação de agentes sociais; crianças e adolescentes; busca de alternativas e populações de rua. Nesta reunião definiu-se a composição a primeira diretoria. No dia 28 de novembro de 1998, com a presença de 50 representantes das pastorais sociais, foi eleita a primeira diretoria e aprovado o Estatuto Social da CAD.
