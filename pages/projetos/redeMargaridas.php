                    <h2>Objetivo Geral</h2>
                    <p>Criar uma rede de solidariedade pela doação de alimentos não perecíveis para com as famílias mais vulneráveis da cidade de Diamantina.</p>
                    <h2>Objetivos Especificos</h2>
                    <ol type="I">
                        <li>Organizar uma rede de pessoas que possam doar mensalmente alimentos para serem doados às pessoas mais necessitadas das comunidades assistidas pela Casa do Cirineu.</li>
                        <li>Despertar a consciência de partilha entre as pessoas de vários segmentos sociais independentemente de sua filiação religiosa.</li>
                        <li>Organizar um Banco de Alimentos de referência para as paróquias da cidade de Diamantina na Casa do Cirineu.</li>
                        <li>Assistir pessoas e entidades com alimentos não perecíveis.</li>
                    </ol>
                    <h2>Metodologia</h2>
                    <p>A “Rede das Margaridas” é uma articulação de grupos formados por 12 pessoas, onde cada uma possa doar mensalmente um item de alimento ou material de limpeza e higiene para compor uma cesta básica de provisões.</p>
                    <p>Faz-se necessário um trabalho de sensibilização e divulgação da proposta entre as lideranças pastorais das comunidades. Cabe a cada paróquia organizar um cadastro das pessoas que vão aderindo ao projeto. Cada grupo de 12 pessoas (por vizinhança, local de trabalho ou estudo, pertença a um grupo de oração ou pastoral, relações de parentesco) vai formar uma “Rede de Margaridas” e escolher um coordenador responsável pela arrecadação dos materiais. Também se pode combinar a entrega dos alimentos no escritório paroquial, identificando a qual rede pertence.</p>
                    <p>Conforme o quadro explicativo anexo (nomes apenas ilustrativos), cada pessoa que compõe a rede compromete-se com a doação do item correspondente a cada mês do ano! É importante ressaltar que a pessoa pode doar a quantidade mínima de um pacote/lata/quilo daquele item. Não pode dar outro item, pois senão vai faltar na composição da cesta de alimentos. Isso não impede que as pessoas possam doar outros itens além daquele estabelecido para o respectivo mês. Com esse método propõe-se que muitas pessoas participem com um pequeno gesto de partilha, não exigindo grandes doações, mas sim pequenos sinais de solidariedade.</p>
                    <p>A Margarida se constitui num elemento simbólico, representando, singeleza, simplicidade. É uma flor desenhada com 12 pétalas e no seu meio o mandato bíblico de Jesus por ocasião do sinal da multiplicação dos pães: “Dai-lhes vós mesmos de comer” (Mc 6, 37). O nome desta flor vem do latim margarita  que significa “pérola”. Apesar de sua fragilidade, ela se adapta a vários tipos de solo. É encantadora e símbolo dos apaixonados. Simbolizou a rede das pastorais sociais articuladas pela dimensão profético-transformadora da ação evangelizadora da Igreja no Brasil. Quem participa da Rede das Margaridas é um apaixonado pelo Evangelho, luta contra o mal querer da indiferença em prol do bem querer solidário com os que mais sofrem.</p>
