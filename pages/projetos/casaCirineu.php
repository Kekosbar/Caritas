                    <h2>Introdução</h2>
                    <p>A Cáritas Arquidiocesana de Diamantina (CAD), a partir de sua Assembleia Geral, realizada em fevereiro de 2017, reorientou sua matriz de planejamento, a partir do novo Marco Estratégico da Cáritas 
                    Brasileira e das suas novas diretrizes aprovadas no V Congresso Nacional, realizado em Aparecida do Norte, em novembro de 2016. Viu-se também a importância de dar uma maior visibilidade às ações da CAD, ampliar a rede 
                    de voluntários e ter um plano de mobilização de recursos para a execução de suas atividades, saindo da exclusividade do Fundo Diocesano de Solidariedade (fruto da Coleta da Campanha da Fraternidade) com fonte de sua manutenção</p>
                    <p>Assim sendo, surge a <b>Casa do Cirineu</b>, ampliando o horizonte de suas finalidades e como um centro visível e concreto de realização das obras de misericórdia, em consonância com os apelos do Papa Francisco, em sua Carta Apostólica
                    <b>Misricordia et Misera</b> no término do Jubileu Extraordinário da Misericórdia:</p>
                    <p>"É a hora de dar espaço à imaginação a propósito da misericórdia, para dar vida a muitas obras novas, fruto da graça. A igreja precisa narrar hoje aqueles 'muitos outros sinais', 
                        que Jesus realizou e que 'não estão escritos"(Jo 20,30) de modo que sejam expressão eloquente da faculdade do amor de Cristo e da comunidade que vive dele. Já se 
                    pasaram mais de dois mil anos e, todavia, as obras de misericórdia continuam a tornar visível a bondade de Deus".</p>
                    <img id="imgPrincipal" src="../../img/projetos/casaCirineu/20180415_080118.jpg" alt="Casa do Cirineu">
                    <br/><br/>
                    <h2>Justificativa</h2>
                    <p>Nos últimos anos, apesar das politicas públicas em vista da erradicação da pobreza no Brasil, vemos na cidade de Diamantina um aumento considerável de pessoas
                    classificadas como socialmente vulneráveis e abaixo da linha de pobreza, pela falta de alternaticas no campo do emprego e geração de renda. Com isso, cresce o número de pessoas que chegam às paróquias, pedindo auxilio para as suas necessidades básicas. 
                    Longe de respaldar práticas assistencialistas, vemos a necessidade e importância dos cristãos católicos organizarem um espaço de acolhida, ternura, consolação e apoio no sofrimento das pessoas que em última instância recorrem as nossas comunidades.</p>
                    <p>A Cáritas Arquidiocesana de Diamantina - CAD, quer ser a "carícia de Deus para com os pobres" como expressou o Papa Francisco em sua carta. Por isso, a CAD propôe-se a organização de um espaço visível e organizado para atendimento dos mais pobres e sofredores em suas necessidades numa ação integrada com as paróquias 
                    da cidade de Diamantina; um espaço para o exercício das obras corporais e espirituais de miserricórdia; um centro referencial de articulação do trabalho voluntário e recepção de recursos materiais e financeiros para o atendimento das pessoas que ali acorrem, evitando a peregrinação das pessoas (as vezes as mesmas) nos escritórios e casas proquiais.</p>
                    <br>
                    <h2>Objetivo Geral</h2>
                    <p>Anunciar o Evangelho pelo testemunho concreto de solidariedade, misericórdia e compaixão, como “cirineus” que ajudam a carregar a cruz de Jesus que está encarnado nos mais pobres e sofredores.</p>
                    
                    <h2>Objetivo Especificos</h2>
                    <ol type="I">
                        <li>Sensibilizar os cristãos e pessoas de boa vontade para a partilha e solidariedade para com as populações mais vulneráveis da cidade de Diamantina.</li>
                        <li>Criar uma rede de voluntários e colaboradores para com as ações da Cáritas Arquidiocesana de Diamantina.</li>
                        <li>Organizar um centro de acolhida e atendimento às pessoas em suas necessidades mais urgentes.</li>
                        <li>Articular as ações de caridade mais imediatas das paróquias da cidade de Diamantina.</li>
                        <li>Organizar um banco de dados do público alvo, evitando a duplicidade de ações isoladas e pouco eficazes.</li>
                        <li>Apresentar à sociedade o rosto samaritano e misericordioso da Igreja que, como Cirineu, ajuda a carregar a cruz dos que sofrem.</li>
                        <li>Despertar diversas categorias de profissionais liberais para a solidariedade e prestação de serviços gratuitos para as pessoas que não têm acesso aos mesmos.</li>
                        <li>Proporcionar aos usuários um espaço de capacitação para o trabalho e geração de renda, acesso às políticas públicas de assistência social, defesa e promoção de seus direitos.</li>
                    </ol>
