            <h2>INTRODUÇÃO</h2>
            <p>A Caritas Arquidiocesana de Diamantina (CAD), fundada em 1998 é uma entidade civil de direito privado, sem fins lucrativos, de duração indeterminada, de caráter filantrópico e de assistência social, com sede no município de Diamantina. Atua no âmbito da Arquidiocese de Diamantina, com 34 municípios e 55 paróquias. É vinculada à Caritas Brasileira e tem como finalidade exercer atividades na defesa dos direitos humanos e do desenvolvimento sustentável solidário na perspectiva da efetivação das políticas públicas. Para isso desenvolve junto às comunidades e grupos parceiros, encontros e oficinas de qualificação assim como a Feira anual de Economia Popular Solidária, com o objetivo de valorizar os produtos e produtores do Alto Jequitinhonha e Sertão da Arquidiocese de Diamantina.</p>
            <p>As ações da CAD se inserem no quadro do novo Marco Estratégico Institucional da Caritas Brasileira elaborado a partir das conclusões do V Congresso Nacional da Caritas, realizado de 09 a 13 de novembro de 2016 em Aparecida do Norte – SP; o planejamento das atividades da Caritas Regional de Belo Horizontes e as demandas e sugestões apresentadas pelas Assembleias Gerais da CAD, realizadas anualmente e o trabalho de Planejamento, Monitoramento, Avaliação e Sistematização (PMAS) desenvolvido por equipe delegada para esse fim.</p>
            
            <h2>MISSÃO INSTITUCIONAL</h2>
            <p>Testemunhar e anunciar o Evangelho de Jesus Cristo, defendendo e promovendo toda forma de vida e participando da construção solidária da sociedade do Bem Viver, sinal do Reino de Deus, junto às pessoas em situação de vulnerabilidade e exclusão social no território da Arquidiocese de Diamantina.</p>
            
            <h2>OBJETIVO GERAL E FINALIDADES DA CAD (cf. Art.2º do Estatuto Social)</h2>
            <p>A CAD tem por objetivo realizar o atendimento, o assessoramento e a defesa e garantia de direitos, tudo na forma do Decreto Federal 6.308/2007, tendo para tanto as seguintes finalidades:</p>
            <ol type="I">
                <li>Realizar estudos referentes à problemática social, educacional e promocional, buscando as soluções adequadas a cada realidade; bem como aprovação e acompanhamento que vise viabilizar projetos sociais.</li>
                <li>Promover e articular ações sociais, especialmente relacionadas com mulheres, crianças, adolescentes, idosos, pessoas com deficiência.</li>
                <li>Estabelecer políticas de parcerias que visem a geração de renda e combatam o desemprego e a pobreza.</li>
                <li>Desenvolver iniciativas na área das pastorais sociais, educação de base e promoção dos direitos humanos.</li>
                <li>Formar e capacitar agentes para a ação social e o exercício da cidadania.</li>
                <li>Promover a solidariedade e a justiça social no meio rural e urbano, com métodos próprios de acordo com a sua realidade.</li>
                <li>Incentivar grupos de proteção ao meio ambiente, agricultura, associações e grupos informais.</li>
                <li>Incentivar os grupos de valorização cultural.</li>
                <li>Desenvolver ações para a promoção da política habitacional de interesse social.</li>
                <li>Providenciar mediante convênios, campanhas ou quaisquer meios adequados, recursos humanos, materiais e financeiros para a realização de seus programas e atendimento das entidades filiadas, bem como para socorro a vitimas de catástrofes.</li>
                <li>Prestar assistência técnica e social ao grupos e comunidades quando solicitado.</li>
            </ol>
            
