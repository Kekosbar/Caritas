            <h2>ÁREAS DE ATUAÇÃO PRIORITÁRIAS NA ARQUIDIOCESE DE DIAMANTINA</h2>
            <h3>Economia Popular Solidária - EPS </h3>
            <p>Tem como objetivo incentivar a agricultura familiar e fortalecer as cooperativas e associações de trabalhadores (as), construindo um modelo de desenvolvimento sustentável solidário baseado em redes e que valorize a troca de saberes, relações mais justas, solidárias e responsáveis entre homens e mulheres, e deles com o meio ambiente em todo o elo da cadeia produtiva. Busca-se a articulação com os fóruns regional e estadual de EPS.</p>
            <h3>Infância, adolescência e juventudes</h3>
            <p>Nessa área de atuação tem-se como princípios o protagonismo juvenil, a educação popular em direitos humanos. O PIAJ – Programa Infância, Adolescência e Juventudes parte do princípio que as crianças, adolescentes e jovens são sujeitos de direitos, desejos, saberes, que podem e devem atuar em suas comunidades, como também na própria vida, compartilhando experiências, participando das articulações e mobilizações sociais, promovendo e animando campanhas de enfrentamento à violência e exploração sexual de crianças e adolescentes, provocando a sociedade para a defesa de seus direitos.</p>
            <h3>Formação permanente do voluntariado</h3>
            <p>A CAD para desenvolver suas atividades no imenso território da Arquidiocese de Diamantina precisa de um contingente de voluntários/as para atuarem na organização das Caritas Paroquiais e em suas diferentes frentes de ação. Por isso visamos criar espaços de capacitação, espiritualidade e acompanhamento dos voluntários.</p>
